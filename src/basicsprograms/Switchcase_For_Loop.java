package basicsprograms;
//Assignment 34
public class Switchcase_For_Loop 
{

	public static void main(String[] args) 
	{
		for (int a=1;a<=2;a++)
			
		{
		  switch (a) 
		         {
		         case 1: 
		 			System.out.println("Chrome Browser Launch");
		 			break; //if no break then chrome and edge both will launch
		 			
		 		case 2:
		 			System.out.println("Edge Browser Launch");
		 			break;
		 			
		 		case 3:
		 			System.out.println("Safari Browser Launch");
		 			break;
		 			
		 		case 4:
		 			System.out.println("Internet Explorer Browser Launch");
		 			break;
		 		
		 		case 5:
		 			System.out.println("Mozilla Firefox Browser Launch");
		 			break;
		 			
		 		default:
		 			System.out.println("Your selection is wrong, kindly press 1,2,3,4 or 5 only");
		 			
		         }
		                
		

	}

	}
	}
